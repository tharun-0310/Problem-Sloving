import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	Scanner sc=new Scanner(System.in);
	int n=sc.nextInt();
	
	for(int i=n;i>0;i--)
	{
	    for(int j=i;j>0;j--)
	    {
	        System.out.print(j);
	    }
	    System.out.println(" ");
	}

	}
}
